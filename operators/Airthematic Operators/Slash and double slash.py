# / operator is used to divide left operand by the righ one.

a = 55
b = 4
result1 = a / b
print("result1:", result1)

# // operator is used to find the quotient.

result2 = a // b
print("result2:", result2)


# / operator working with floating-point numbers.

result3 = 55.5 / 4
print("result3:", result3)

# // operator working with floating-point numbers.

result4 = 55.5 // 4
print("result4:", result4)