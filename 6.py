# strings

# single quoted strings
a='Tarun'
print(a)
print(type(a))

# double quoted strings == in double quotes single quotes can also being printed.
b="Tarun"
print(b)
print(type(b))

# triple quoted strings == in triple quotes single and double quotes can also being printed.
b='''t"aru'n'''
print(b)
print(type(b))