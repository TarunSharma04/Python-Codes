# An expression containing or operator evaluates to True if either of the operands is true.

# print( True or True)   = True
# print(False or True)   = True
# print(False or False)  = False



# Example:

a = 2
b= 3
c= 5

print(a<b or b>c)
print(b<a or b<c)
print(b>c or a>b)
print(a==4 or b!=2)