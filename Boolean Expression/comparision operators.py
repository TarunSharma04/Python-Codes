# > Greater than    6>5     True
# < Less than       6<5     False
# == Equal to       6==5    False
# != Not equal to   6!=5    True
# >= Greater than   6>=5    True
#    or equal to
# <= Less than or   6<=5    True
#    equal to.


#Examples: 

x = 10
y = 12

print(x > y)
print(x < y)

print(x == y)
print(x == 10)

print(x != y)
print(x != 55)

print(x >= y)
print(x >= 14)

print(x <= y)
print(x <= 10)